
public class Player {
	private String name;
	private int totalscore;

	public String getName() {
		return name;
	}

	
	public void setName(String name) {
		this.name = name;
	}


	public int getScore() {
		return totalscore;
	}


	public void setScore(int score) {
		this.totalscore = score;
	}
	
}
